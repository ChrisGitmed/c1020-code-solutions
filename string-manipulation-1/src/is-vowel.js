function isVowel(character) {
  if (character === 'A' || character === 'a' || character === 'E' || character === 'e' ||
    character === 'I' || character === 'i' || character === 'O' || character === 'o' ||
    character === 'U' || character === 'u') {
    return true;
  }
  return false;
}
